export const chinaGeoJson = {
  type: 'FeatureCollection',
  features: cells.map(([code, name, x, y, w, h]) => ({
    type: 'Feature',
    properties: { id: code, name },
    geometry: {
      type: 'Polygon',
      coordinates: [[
        [x, -y], [x + w, -y], [x + w, -(y + h)], [x, -(y + h)], [x, -y]
      ]]
    }
  }))
}

export function provinceName(code) {
  return provinces.find((item) => item.code === code)?.name || code
}
